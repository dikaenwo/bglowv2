export function createBottomNav(activeTab = 'home') {
  const nav = document.createElement('nav');
  nav.className = 'bottom-nav';
  nav.innerHTML = `
    <button class="nav-item ${activeTab === 'home' ? 'active' : ''}" data-page="home">
      ${icons.home}
      <span>Beranda</span>
    </button>
    <button class="nav-item ${activeTab === 'routine' ? 'active' : ''}" data-page="routine">
      ${icons.routine}
      <span>Rutinitas</span>
    </button>
    <button class="nav-item ${activeTab === 'scan' ? 'active' : ''} scan-btn" data-page="scan">
      <div class="scan-icon-wrap">${icons.brain}</div>
      <span>Scan AI</span>
    </button>
    <button class="nav-item ${activeTab === 'recommendations' ? 'active' : ''}" data-page="recommendations">
      ${icons.star}
      <span>Rekomendasi</span>
    </button>
    <button class="nav-item ${activeTab === 'profile' ? 'active' : ''}" data-page="profile">
      ${icons.profile}
      <span>Profil</span>
    </button>
  `;

  nav.querySelectorAll('.nav-item').forEach(item => {
    item.addEventListener('click', () => {
      const page = item.dataset.page;
      window.location.hash = `#/${page === 'home' ? '' : page}`;
    });
  });

  return nav;
}
